const translations = { fr, en, es };

export type Language = 'fr' | 'en' | 'es';

interface I18nContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, options?: any) => any;
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

const interpolate = (str: string, options: { [key: string]: string | number | undefined }) => {
  let result = str;
  for (const key in options) {
    if (options[key] !== undefined) {
      result = result.replace(new RegExp(`{{${key}}}`, 'g'), String(options[key]));
    }
  }
  return result;
};

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>('fr');

  useEffect(() => {
    const savedLang = localStorage.getItem('appLanguage') as Language;
    if (savedLang && ['fr', 'en', 'es'].includes(savedLang)) {
      setLanguage(savedLang);
    }
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
    localStorage.setItem('appLanguage', language);
  }, [language]);

  const t = (key: string, options?: any) => {
    const langData = language === 'fr' ? fr : en;

    // Support for nested keys (e.g., 'onboarding.goals.weight_loss')
    const keys = key.split('.');
    let translation: any = langData;

    for (const k of keys) {
      if (translation && typeof translation === 'object' && k in translation) {
        translation = translation[k];
      } else {
        translation = null;
        break;
      }
    }

    if (!translation) return key;

    if (options?.returnObjects) {
      return translation;
    }

    if (typeof translation !== 'string') return key;

    const interpolate = (text: string, params: any) => {
      let result = text;
      Object.keys(params).forEach(p => {
        result = result.replace(new RegExp(`{{${p}}}`, 'g'), params[p]);
      });
      return result;
    };

    return options ? interpolate(translation, options) : translation;
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useTranslation must be used within an I18nProvider');
  }
  return context;
};